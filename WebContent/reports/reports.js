function checkForm5()
{
	var error = 0;
	if($(".error").css('display') == 'compact')
	{
		$(".error").attr("style", "display: none");
		$(".error ul").empty();
	}
	
	if($("select#groups5").val() == "0")
	{
		$('.error ul').append("<li>יש לבחור קבוצה מהרשימה</li>");
		error = 1; 
	}
	
	if(error == 1)
	{
		$(".error").attr("style", "display: compact");
		return false;
	}
	
	return true;
}

